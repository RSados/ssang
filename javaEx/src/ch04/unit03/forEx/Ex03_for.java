package ch04.unit03.forEx;

public class Ex03_for {
	public static void main(String[] args) {
		int n;
		for(n=1;n<=10;n+=2) {
			System.out.print(n+" ");//1 3 5 7 9
		}
		System.out.println("\n밖:"+n);
		System.out.println();
		
		for(n=2;n<=10;n+=2) {
			System.out.print(n+" ");//1 3 5 7 9
		}
		System.out.println("\n밖:"+n);
		System.out.println();
	}

}
